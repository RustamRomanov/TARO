/** Подсказки для экрана выбора (заголовок, описание, время, рекомендуемые колоды). */
export function getSpreadHint(spreadId) {
  const spread = SPREADS.find((s) => s.id === spreadId);
  if (!spread) return null;
  const recommendedDeckIds = SPREAD_RECOMMENDED_DECKS[spreadId] || [];
  const cardsLabel = spread.id === 'single' ? '1 карта' : pluralCards(spread.cards);
  return {
    title: spread.name,
    cardsLabel,
    description: spread.hint,
    time: spread.time,
    bestDeckId: recommendedDeckIds[0] || null,
    recommendedDeckIds,
    positions: spread.positions,
  };
}

export function getSpreadById(spreadId) {
  if (spreadId === 'magic_ball') {
    return { id: 'magic_ball', name: 'Магический шар', icon: '🔮', cards: 0, time: '1 мин', hint: 'Быстрый ответ да или нет', positions: [] };
  }
  return SPREADS.find((s) => s.id === spreadId) || SPREADS[0];
}

